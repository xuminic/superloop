int bai_uart_send_sleep(void *uhandle, int ms)
{
	uart_t	*ufp = uhandle;
	uint32_t  rc;

	if ((ufp->taskid = osThreadGetId()) != NULL) {
		rc = ulTaskNotifyTake(pdTRUE, 
				ms == 0 ? portMAX_DELAY : pdMS_TO_TICKS(ms));
		if (rc <= 0) {
			return -1;	/* timeout */
		}
		return 0;	/* wake up by ISR */
	}

	if (ms == 0) {	/* infinite sleep */
		while (ufp->state & FCMD_SEND) osDelay(1);
	} else {
		while (ms-- && (ufp->state & FCMD_SEND)) osDelay(1);
	}
	if (ufp->state & FCMD_SEND) {
		return -1;	/* timeout */
	}
	return 0;	/* wake up by ISR */
}

void bai_uart_send_awake(void *huart)
{
	BaseType_t	xHigherWoken = pdFALSE;
	xtcb_t		*xtcb = bai_get_uart_xtcb(huart);
	
	xtcb->uart->state &= ~FCMD_SEND;
	if (xtcb->uart->taskid) {
		vTaskNotifyGiveFromISR(xtcb->uart->taskid, &xHigherWoken);
		portYIELD_FROM_ISR(xHigherWoken);
	}
}

int bai_uart_receive_sleep(void *uhandle, int ms)
{
	uart_t  *ufp = uhandle;
	xtcb_t	*xtcb = bai_get_uart_xtcb(ufp->handler);
	uint32_t  rc;

	if (xtcb->taskid) {
		rc = ulTaskNotifyTake(pdTRUE,
				ms == 0 ? portMAX_DELAY : pdMS_TO_TICKS(ms));
		if (rc <= 0) {
			return -1;	/* timeout */
		}
		return 0;	/* wake up by ISR */
	}

	if (ms == 0) {	/* infinite sleep */
		while (ufp->state & FCMD_RECV) osDelay(1);
	} else {
		while (ms-- && (ufp->state & FCMD_RECV)) osDelay(1);
	}
	if (ufp->state & FCMD_RECV) {
		return -1;	/* timeout */
	}
	return 0;	/* wake up by ISR */
}

void bai_uart_receive_awake(void *huart, int state)
{
	BaseType_t	xHigherWoken = pdFALSE;
	xtcb_t		*xtcb = bai_get_uart_xtcb(huart);
	
	if (xtcb->uart->state & FCMD_RECV) {
		if (state < 0) {
			xtcb->uart->state &= ~FCMD_RECV;
		}
		if (xtcb->taskid) {
			vTaskNotifyGiveFromISR(xtcb->taskid, &xHigherWoken);
		}
		portYIELD_FROM_ISR(xHigherWoken);
	}
}


